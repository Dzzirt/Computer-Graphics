public class FloatRect {

    public FloatRect() {
    }

    public FloatRect(float x, float y, float w, float h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }

    public float x = 0;
    public float y = 0;
    public float w = 0;
    public float h = 0;
}
